public class Exercicio01OpeAri {

    public static void main(String[] args) {

        //Declarar as variáveis
        int a;
        int b;

        a = 15;
        b = 7;

        //Soma
        System.out.println("O resultado da soma dos valores 15 e 7 é: " + (a+b));

        //Subtração
        System.out.println("O resultado da subtração dos valores 15 e 7 é: " + (a-b));

        //Multiplicação
        System.out.println("O resultado da multiplicação dos valores 15 e 7 é: " + (a*b));

        //Divisão
        System.out.println("O resultado da divisão dos valores 15 e 7 é: " + (a/b));
        
    }
}