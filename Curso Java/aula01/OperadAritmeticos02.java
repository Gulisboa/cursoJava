public class OperadAritmeticos02 {

    public static void main(String[] args) {

        // Atalho para execucar: CTRL + F5

        // operadores aritméticos: +; -; /; *; %;(resto da divisão)

        // syso = abreviacao de systemoutprintln
        System.out.println(2 + 3 * 4);

        System.out.println((2 + 3) * 4);

        // É necessário colocar a conta em parentesis para ele priorizar e nao agregar
        // ao invés de somar
        System.out.println("A soma de 2 + 4 é: " + (2 + 4));

        // quebrar linha: \n
        System.out.println("Este texto \ndeve ser quebrado \nem várias linhas");

    }

}